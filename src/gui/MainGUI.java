    private void jTextField_TamanoNumberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_TamanoNumberKeyPressed
        // TODO add your handling code here:
        try {
            
            if(Integer.parseInt(jTextField_TamanoNumber.getText())%2 != 0){
                jTextField_TamanoNumber.setText(String.valueOf(Integer.parseInt(jTextField_TamanoNumber.getText()) + 1));
            }
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                jSlider_Tamano.setValue(Integer.parseInt(jTextField_TamanoNumber.getText()));
            }
        } catch (Exception e) {
            jTextField_TamanoNumber.setText("10");
            jSlider_Tamano.setValue(Integer.parseInt(jTextField_TamanoNumber.getText()));
        }
    }//GEN-LAST:event_jTextField_TamanoNumberKeyPressed

    private void jSlider_TamanoMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jSlider_TamanoMouseReleased
        // TODO add your handling code here:
        jTextField_TamanoNumber.setText(String.valueOf(jSlider_Tamano.getValue()));
        
        jSlider_Tiempo.setMinorTickSpacing(1);
        jSlider_Tiempo.setMaximum(((jSlider_Tamano.getValue() - 4)/2));
        jSlider_Tiempo.setValue(1);
        jTextField_TiempoNumber.setText(String.valueOf(jSlider_Tiempo.getValue()));
    }//GEN-LAST:event_jSlider_TamanoMouseReleased

    private void jTextField_IteracionNumberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_IteracionNumberKeyPressed
        // TODO add your handling code here:
        try {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                if(Integer.parseInt(jTextField_IteracionNumber.getText()) > 1001){
                    jSlider_Iteracion.setValue(1001);
                    jTextField_IteracionNumber.setText("1001");
                }
                else if(Integer.parseInt(jTextField_IteracionNumber.getText()) < 1){
                    jSlider_Iteracion.setValue(1);
                    jTextField_IteracionNumber.setText("1");
                }
                else{
                    jSlider_Iteracion.setValue(Integer.parseInt(jTextField_IteracionNumber.getText()));
                }
            }
        } catch (Exception e) {
            jTextField_IteracionNumber.setText("10");
            jSlider_Iteracion.setValue(Integer.parseInt(jTextField_IteracionNumber.getText()));
        }
    }//GEN-LAST:event_jTextField_IteracionNumberKeyPressed

