    private void jTextField_TiempoNumberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_TiempoNumberKeyPressed
        // TODO add your handling code here:
        try {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                jSlider_Tiempo.setValue(Integer.parseInt(jTextField_TiempoNumber.getText()));
            }
        } catch (Exception e) {
            jTextField_TiempoNumber.setText("10");
            jSlider_Tiempo.setValue(Integer.parseInt(jTextField_TiempoNumber.getText()));
        }
        
    }//GEN-LAST:event_jTextField_TiempoNumberKeyPressed

